def main():
    conn = sqlite3.connect(DB_PATH)
    conn.execute("PRAGMA journal_mode=WAL")
    cursor = conn.cursor()

    # ユーザーが存在するか確認、なければ作成
    cursor.execute("SELECT id FROM users WHERE id = ?", (USER_ID,))
    if cursor.fetchone() is None:
        cursor.execute(
            "INSERT INTO users (id, nickname, created_at) VALUES (?, ?, ?)",
            (USER_ID, "デモユーザー", now_iso()),
        )
        print(f"Created user: {USER_ID}")

    inserted = 0
    for doc in DOCUMENTS:
        doc_id = str(uuid.uuid4())
        ts = now_iso()
        ext = doc["filename"].rsplit(".", 1)[-1]

        cursor.execute(
            """INSERT INTO documents
            (id, knowledge_base_id, filename, file_type, original_path,
             converted_md, version, status, retry_count,
             uploaded_by, uploaded_at)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (
                doc_id,
                doc["kb"],
                doc["filename"],
                ext,
                f"/app/uploads/{doc_id}_{doc['filename']}",
                doc["content"],
                1,
                "converted",  # インデックスなしだがコンテンツは閲覧可能
                0,
                USER_ID,
                ts,
            ),
        )
        inserted += 1
        print(f"Inserted: {doc['filename']} -> {doc_id}")

    conn.commit()

    # 確認: 各KBのドキュメント数
    for kb_name, kb_id in [
        ("製造プロセスマニュアル", KB_PROCESS),
        ("安全衛生ガイドライン", KB_SAFETY),
        ("トラブルシューティング", KB_TROUBLE),
    ]:
        cursor.execute(
            "SELECT COUNT(*) FROM documents WHERE knowledge_base_id = ? AND deleted_at IS NULL",
            (kb_id,),
        )
        count = cursor.fetchone()[0]
        print(f"{kb_name}: {count} documents")

    conn.close()
    print(f"\nTotal inserted: {inserted} documents")


if __name__ == "__main__":
    main()
